import '../css/frontend.css';
import Alpine from 'alpinejs';
import { createApp } from 'vue';

// Initialize Alpine.js
window.Alpine = Alpine;
Alpine.start();

// Import frontend components
import FrontendApp from './components/frontend/FrontendApp.vue';

// Create Vue app for frontend if the element exists
if (document.getElementById('frontend-app')) {
    const app = createApp(FrontendApp);
    app.mount('#frontend-app');
}

// Initialize Bootstrap components
document.addEventListener('DOMContentLoaded', () => {
    // Initialize tooltips
    const tooltipTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="tooltip"]'));
    tooltipTriggerList.map(function (tooltipTriggerEl) {
        return new bootstrap.Tooltip(tooltipTriggerEl);
    });
    
    // Initialize popovers
    const popoverTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="popover"]'));
    popoverTriggerList.map(function (popoverTriggerEl) {
        return new bootstrap.Popover(popoverTriggerEl);
    });
    
    // Smooth scrolling for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    });
});
